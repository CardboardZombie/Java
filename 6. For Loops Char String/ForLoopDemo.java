import java.util.Scanner;

public class ForLoopDemo
{
	public static void main(String[] args)
	{
		for(int i = 0; i < 10; i++)
		{
			System.out.println("Hello World");
		}
		
		int c = 0;
		while(c < 10)
		{
			System.out.println("Hello World");
			c++;
		}
	
	}
}