public class IncrementTest
{
	public static void main(String[] args)
	{
		
		int number = 26;
		number++;
		System.out.println("Number: "+number);
		number++;
		System.out.println("Number: "+number);
		number--;
		System.out.println("Number: "+number);
		number--;
		System.out.println("Number: "+number);
	}
}